var rowLength = treeArray[0].length;

function slopeCheckDeux( trees, right, down ) {
  var i = 0;
  var i2 = 0;
  var treeCount = 0;
  trees.forEach( (tree) => {
    var downOffset = i % down;
    // console.log(downOffset);
    if ( downOffset === 0 ) {
      var pointer = ( ( i2 * right ) + 1 ) % rowLength;
      // console.log(pointer);

      var pointerIndex = pointer === 0 ? rowLength - 1 : pointer -1;
      // console.log({pointerIndex});

     if ( tree.charAt( pointerIndex ) === '#' ) {
       treeCount++;
     };
     i2++;
    }
    i++;
  });
  return treeCount;
}

